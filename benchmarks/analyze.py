#!/usr/bin/env python3
"""
benchmarks/analyze.py
─────────────────────────────────────────────────────────────────
Aggregate every JSON in benchmarks/results/ into a single summary
table that we ship as benchmarks/SUMMARY.md.

This is what turns "Plumb works on six toy testcases" into "Plumb
works on 83 programs from LLVM's official test-suite, here are the
findings."
"""
from __future__ import annotations
import json, pathlib, statistics, sys, collections, re

ROOT       = pathlib.Path(__file__).resolve().parent
RESULTS    = ROOT / "results"
OUT_FILE   = ROOT / "SUMMARY.md"

def load_all() -> list[dict]:
    runs = []
    for p in sorted(RESULTS.glob("*.json")):
        m = re.match(r"(.+)\.(O0|O2)\.json$", p.name)
        if not m:
            continue
        prog, lvl = m.group(1), m.group(2)
        try:
            d = json.loads(p.read_text())
        except json.JSONDecodeError:
            continue
        runs.append({
            "prog": prog,
            "lvl":  lvl,
            "tot":  d["metadata"]["totals"]["totalCost"],
            "fns":  d["functions"],
            "raw":  sum(g["count"] for f in d["functions"] for g in f["groups"]),
            "wgt":  sum(g["count"] * d["metadata"]["weights"].get(g["group"],0)
                        for f in d["functions"] for g in f["groups"]),
            "energy": d["metadata"]["totals"].get("estimatedEnergyPj", 0),
        })
    return runs

def by_prog(runs):
    """Returns {prog: {lvl: run}}."""
    out = collections.defaultdict(dict)
    for r in runs:
        out[r["prog"]][r["lvl"]] = r
    return out

def fmt_int(n):
    return f"{int(n):,}"

def main():
    runs = load_all()
    if not runs:
        sys.exit("no JSON results found — run ./benchmarks/run_bench.sh first")

    progs = by_prog(runs)
    n_pairs = sum(1 for p in progs.values() if "O0" in p and "O2" in p)
    n_total = len(runs)
    n_fns   = sum(len(r["fns"]) for r in runs)

    # ── Aggregate stats over all O0 runs ────────────────────────
    o0_runs = [r for r in runs if r["lvl"] == "O0"]
    o2_runs = [r for r in runs if r["lvl"] == "O2"]

    def pct_change(a, b):
        return (b - a) / a * 100 if a else 0

    # Per-program O0 → O2 deltas (programs that have both levels)
    deltas = []
    for prog, lvls in progs.items():
        if "O0" in lvls and "O2" in lvls:
            deltas.append({
                "prog": prog,
                "o0":   lvls["O0"]["tot"],
                "o2":   lvls["O2"]["tot"],
                "delta": lvls["O2"]["tot"] - lvls["O0"]["tot"],
                "pct":  pct_change(lvls["O0"]["tot"], lvls["O2"]["tot"]),
            })

    # Programs whose -O2 cost INCREASED (the failure mode we documented in
    # EVALUATION.md §5.1 — see how often it happens in the wild)
    regressions = [d for d in deltas if d["pct"] > 0]
    median_drop = statistics.median([d["pct"] for d in deltas])
    biggest_drop = min(deltas, key=lambda d: d["pct"])
    biggest_rise = max(deltas, key=lambda d: d["pct"])

    # Group dominance — for each program, what is the most expensive group?
    dominant = collections.Counter()
    for r in o0_runs:
        groups = collections.defaultdict(int)
        for f in r["fns"]:
            for g in f["groups"]:
                groups[g["group"]] += g["cost"]
        if groups:
            dominant[max(groups, key=groups.get)] += 1

    # Top-level "hottest function" per program — what fraction is named
    # something that obviously sounds like a kernel? This is a softer
    # cross-check that "the hot function makes intuitive sense."
    hot_fn_names = []
    for r in o0_runs:
        if not r["fns"]:
            continue
        hot = max(r["fns"], key=lambda f: f["totalCost"])
        hot_fn_names.append((r["prog"], hot["name"], hot["totalCost"]))

    # Per-suite breakdown
    suite_of = lambda prog: prog.split("__", 1)[0] if "__" in prog else "Other"
    suite_stats = collections.defaultdict(lambda: {"n":0, "cost":0})
    for r in o0_runs:
        s = suite_of(r["prog"])
        suite_stats[s]["n"] += 1
        suite_stats[s]["cost"] += r["tot"]

    # ── Write SUMMARY.md ────────────────────────────────────────
    with open(OUT_FILE, "w") as f:
        w = f.write
        w(f"# Plumb on LLVM's test-suite\n\n")
        w(f"> Auto-generated by `benchmarks/analyze.py` from "
          f"`benchmarks/results/*.json`.\n"
          f"> Don't hand-edit — re-run `./benchmarks/run_bench.sh && "
          f"python3 benchmarks/analyze.py` after any pass change.\n\n")

        w(f"This file aggregates Plumb's output across **{len(progs)} programs** "
          f"from the official [LLVM test-suite](https://github.com/llvm/llvm-test-suite) "
          f"`SingleSource/Benchmarks` subset (Stanford, Misc, Polybench, "
          f"Shootout). Each program is analysed at both `-O0` and `-O2`, "
          f"giving **{n_total} run(s)** covering **{n_fns:,} function(s)** "
          f"of real-world C/C++ code.\n\n")

        w(f"## Headline numbers\n\n")
        w(f"| Metric | Value |\n|---|---:|\n")
        w(f"| Programs successfully analysed | **{len(progs)}** |\n")
        w(f"| O0 + O2 runs                    | **{n_total}** |\n")
        w(f"| Total functions covered         | **{n_fns:,}** |\n")
        w(f"| Median cost reduction at -O2    | **{median_drop:.1f}%** |\n")
        w(f"| Programs where -O2 cost rose¹   | **{len(regressions)}/{len(deltas)}** |\n")
        w(f"| Biggest cost reduction          | `{biggest_drop['prog']}` ({biggest_drop['pct']:.1f}%) |\n")
        w(f"| Biggest cost increase           | `{biggest_rise['prog']}` (+{biggest_rise['pct']:.1f}%) |\n\n")
        w(f"¹ The -O2 cost increase is the inliner-inflation failure mode "
          f"documented in [EVALUATION.md §5.1](../EVALUATION.md#5-failure-cases). "
          f"At scale, it affects "
          f"**{len(regressions)/len(deltas)*100:.0f}%** of programs.\n\n")

        w(f"## Cost-class dominance (-O0)\n\n")
        w(f"For each program at `-O0`, which instruction group dominated "
          f"its weighted cost?\n\n")
        w(f"| Group | Programs | Share |\n|---|---:|---:|\n")
        for grp, n in dominant.most_common():
            share = n / sum(dominant.values()) * 100
            w(f"| `{grp}` | {n} | {share:.0f}% |\n")
        w("\n")

        w(f"## Per-suite summary\n\n")
        w(f"| Suite | Programs | Σ cost (O0) | mean cost / program |\n")
        w(f"|---|---:|---:|---:|\n")
        for s, st in sorted(suite_stats.items()):
            mean = st["cost"] / max(st["n"], 1)
            w(f"| {s} | {st['n']} | {fmt_int(st['cost'])} | {mean:,.0f} |\n")
        w("\n")

        w(f"## Top 15 hottest programs (by total weighted cost @ -O0)\n\n")
        w(f"| Rank | Program | Cost (O0) | Cost (O2) | Δ% | Hottest function |\n")
        w(f"|---:|---|---:|---:|---:|---|\n")
        ranked = sorted(o0_runs, key=lambda r: -r["tot"])[:15]
        for i, r in enumerate(ranked, 1):
            o2 = progs.get(r["prog"], {}).get("O2", {}).get("tot", "—")
            pct = (
                f"{(o2 - r['tot'])/r['tot']*100:+.0f}%"
                if isinstance(o2, (int, float)) and r["tot"] else "—"
            )
            hot = max(r["fns"], key=lambda x: x["totalCost"]) if r["fns"] else None
            hot_str = f"`{hot['name']}` ({hot['totalCost']})" if hot else "—"
            o2_str = fmt_int(o2) if isinstance(o2, (int, float)) else o2
            w(f"| {i} | `{r['prog']}` | {fmt_int(r['tot'])} | {o2_str} | "
              f"{pct} | {hot_str} |\n")
        w("\n")

        w(f"## Programs where -O2 *increased* cost\n\n")
        w(f"This is the static-model blind spot we documented in "
          f"[EVALUATION.md §5.1](../EVALUATION.md#5-failure-cases) — "
          f"-O2 inlining + unrolling pulls callees into the caller, so "
          f"the per-function cost goes up even though wall-clock goes "
          f"down. At scale this happens in "
          f"{len(regressions)} of {len(deltas)} programs "
          f"({len(regressions)/len(deltas)*100:.0f}%).\n\n")
        w(f"| Program | O0 | O2 | Δ |\n|---|---:|---:|---:|\n")
        for r in sorted(regressions, key=lambda d: -d["pct"])[:10]:
            w(f"| `{r['prog']}` | {fmt_int(r['o0'])} | {fmt_int(r['o2'])} | "
              f"+{r['pct']:.0f}% |\n")
        w("\n")

        w(f"## Reproduce\n\n")
        w(f"```bash\n")
        w(f"./build.sh                       # build Plumb\n")
        w(f"./benchmarks/fetch.sh            # sparse-clone test-suite\n")
        w(f"./benchmarks/run_bench.sh        # ~20 s on Apple Silicon\n")
        w(f"python3 benchmarks/analyze.py    # regenerates this file\n")
        w(f"```\n")

    print(f"✓ wrote {OUT_FILE}")
    print(f"  programs : {len(progs)}")
    print(f"  runs     : {n_total}")
    print(f"  functions: {n_fns}")

if __name__ == "__main__":
    main()
